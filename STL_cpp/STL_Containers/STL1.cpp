// Container library is a collection of classes
//
// Containers are implemented as generic class templates
// 
// " Container								   Classes"  -------> "represents"
//   vector		: arrays
//   queue		: queues
//   stack		: stack
//   priority_queue	: heap
//   list		: linked list
//   set		: trees
//   map		: associative arrays
//
// Classification of Containers
//
// 1) Sequence containers-> linear data structure ,data gets stored one by one not randomly--> array,linked list,etc
//
// 2) Associative containers-> sorted data structures like map,set,etc
//
// 3) Unordered Associative containers-> unsorted Data Structures
//
// 4) Containers Adapters-> interfaces to sequence containers
